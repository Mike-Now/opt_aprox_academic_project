package opt;

public class Report {
	public final double grossSalesIncome;
	public final double primeCosts;
	public final double salesIncome;
	public Report(double grossSalesIncome, double primeCosts, double salesIncome) {
		super();
		this.grossSalesIncome = grossSalesIncome;
		this.primeCosts = primeCosts;
		this.salesIncome = salesIncome;
	}


}
